namespace app\assets;

use Yii;
use yii\web\AssetBundle;
use yii\web\View;

/**
 * Main application asset bundle.
 *
 * @author Qiang Xue <qiang.xue@gmail.com>
 * @since 2.0
 */
class AppAsset extends AssetBundle
{
    public $basePath = '@webroot';
    public $baseUrl = '@web';

    public $jsOptions = [
        "position" => View::POS_HEAD
    ];

    public $css = [
        'css/style.css',
        'css/main.css',
    ];
    public $js = [
        'holdon/jquery.topzindex.min.js',
        'holdon/HoldOn.min.js',
        'js/class.util.js',
    ];
    public $depends = [
    ];

    public function init()
    {

        $path = Yii::$app->assetManager->publish(Yii::$app->basePath . "/web/kendo/");
        $view = Yii::$app->view;
        $view->registerJsFile($path[1] . "/js/jquery.min.js", ["position" => View::POS_HEAD]);
        $view->registerJsFile($path[1] . "/js/kendo.all.min.js", ["position" => View::POS_HEAD]);


        $view->registerCssFile($path[1] . "/styles/kendo.common.min.css");
        $view->registerCssFile($path[1] . "/styles/kendo.common-bootstrap.min.css");
        $view->registerCssFile($path[1] . "/styles/kendo.bootstrap.min.css");

        parent::init(); // TODO: Change the autogenerated stub
    }

}
